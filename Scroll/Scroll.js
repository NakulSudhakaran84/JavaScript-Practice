function ScrollUp(){
    
    for(var i=1;i<=10;i++){
        window.scrollBy(0,-150);
    }
}

function ScrollDown(){
   for(var i=1;i<=10;i++){
        window.scrollBy(0,10);
    }
}

function ScrollLeft(){
    for(var i=1;i<=10;i++){
        window.scrollBy(-50,0);
    }
}

function ScrollRight(){
    for(var i=1;i<=10;i++){
        window.scrollBy(50,0);
    }
}